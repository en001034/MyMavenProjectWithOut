package app;

public class Calculator {
	
	public static int add(int i, int j) {
		
		int result=i+j;
		return result;
	}
	
    public static int subtract(int i, int j) {
		
		int result=i-j;
		return result;
	}
    
 public static int multipy(int i, int j) {
		
		int result=i*j;
		return result;
	}

}
